package exam02;

public class Ex01 {
	public static void main(String[] args) {
		//자료형 변수 = 값;
		//선언 및 초기화
		byte num = 5;
		System.out.println(num);
		num = 3;
		System.out.println(num);
		num = 100;
		System.out.println(num);
		num = 127;
		System.out.println(num);
		num = -128;
		System.out.println(num);

		byte num2;
		num2 = 20;
		System.out.println(num2);
		
		short s1 = 30;
		System.out.println(s1);
		s1 = 1000;
		System.out.println(s1);
		
		int i1 = 50000;
		System.out.println(i1);
	}
}
