package exam03;

public class Ex02 {
	public static void main(String[] args) {
		//비교연산자 : > >= < <= == !=
		int n1 = 7;
		int n2 = 8;
		
		System.out.println(n1 > n2);
		System.out.println(n1 >= n2);
		System.out.println(n1 < n2);
		System.out.println(n1 <= n2);
		System.out.println(n1 == n2);
		System.out.println(n1 != n2);
		
		System.out.println((n1 + 1) == n2);
		System.out.println((n1 + 1) == (n2 - 1));
		
		boolean result = n1 > n2;
		System.out.println(result);
		
		
		
		
		
		
		
	}
}
