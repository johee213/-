package exam03;

public class Ex04 {
	public static void main(String[] args) {
		//대입연산자 : =
		int k;
		k = 5;
		System.out.println(k);
		
		k++; // k = k + 1;
		System.out.println(k);
		
		++k; // k = k + 1;
		System.out.println(k); //7
		
		k += 1; // k = k + 1;
		System.out.println(k); //8
		
		k -= 1; // k = k - 1;
		System.out.println(k); //7
		
		k *= 2; // k = k * 2;
		System.out.println(k); //14
		
		k /= 2; // k = k / 2;
		System.out.println(k);
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
